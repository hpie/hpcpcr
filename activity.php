              <h2 class="title mb-4">Activities</h2>
              <p>In order to attain the Commission's Mandate of ensuring that each and every child has an access to all entitlements and enjoys all their rights, the State Commission for Protections of Child Rights took suo moto action on POCSO cases, collects action taken report from the quarter concerned immediately, and also helps the victims for immediate relief. </p>
              <p>
				<ul>
					<li>So far as the matter of implementation of POCSO Act, 2012 is concerned, the State Commission for Protection of Child Rights used to organize the above mentioned coordination meetings with the stake holders and used to give training to the people engaged in teaching to sensitize the students regarding safe and unsafe touch. The Commission used to monitor the rehabilitation of the POCSO victims.</li> 

					<li>After the constitution of State Commission one day workshop for sensitizing SMCs on issues of child Rights and Child Protection at HIPA on dated  23-12-2014. Teachers, SMC members and CWC Chairperson/Members of various districts attended this workshop.</li>

					<li>On 09th January, 2015 one day awareness camp was organized at NAC Sunni Renbasera Bhawan or sensitization of teachers and Members of School Management Committee. Approximately 100 participants participated in the camp i.e. Chairmen of SMCs of 15 panchayats, DSP/SHO, SDM, BDO, Tehsil Welfare Officer, BMO, BRCs, BPO, CDPO, 30 Anganwari Workers, Anganwari Supervisor and Advocate/Attorney from Directorate of Prosecution.</li> 

					<li>The International Child Rights day was also organized by the Commission on 20-11-2016 at Sanjauli, Shimla with the collaboration of NCPCR. Children’s of various schools as well as slum area children were also present on this occasion.</li> 

					<li>State Commission organized a regional level sensitization Programme cum Awareness Workshop on Juvenile Justice Act, 2015, the POCSO Act, 2012 with the collaboration of National Commission for Protection of Child Rights at Mandi on 08-12-2016. Approximately 100 participants participated in the programme i.e. Chairmen of SMCs, Panchayats, DSP/SHO, SDM, BDO, Tehsil Welfare Officer, BMO, BRCS, BPO, CDPO, 30 Anganwari Workers, Anganwari Supervisor etc.</li>

					<li>On 27-02-2017, the Commission organized a state level workshop with the collaboration of NCPCR at Shimla. During the opening ceremony, Hon’ble Chief Minister launched a survey on safe and secure school environment and also released a poster on RTE Act, 2009. Different stake holders throughout the state participated in the deliberations.</li> 

					<li>Children Day (Bal Divas) was also organized by the Commission on 14-11-2017 at Solan  The Commission organize Marathon to sensitize the general public/students at large about the Right to Education.</li> 	

					<li>On 19-02-2018,  the Commission organized a zonal level workshop on RTE Act, 2009 at Dharamshala with the collaboration of SSA,  About 200 participants i.e. different stake holders throughout the zone attended this workshop.</li>

					<li>The Commission organized a zonal level workshop on RTE Act, 2009 on 13-03-2018 at Kullu with the collaboration of SSA. About 300 participants i.e. different stake holders throughout the zone attended this workshop. Similarly said workshop was organized at  Chamba during June 2018 with coloration of SSA, HP.</li>

					<li>Essay and painting completion was conducted on the occasion of celebration of 150 years of Mahatma Gandhi at Govt. Sr. Secondary School Kasumpati Shimla-9 on 2nd October, 2019 and workshop on RTE and JJ on this occasion was also conducted at Govt.  College of Excellence Sanjauli Shimla-9 on 18th of October 2019.</li>

					<li>The Commission used to visit the schools, Anganwari kendras, Child Care Institutions from time to time to sensitize the children, stake holders and general public.</li>
				</ul>
